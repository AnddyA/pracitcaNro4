    public void tiempoDemoraQuickSort(Integer tipo){
        
        long starTime = System.currentTimeMillis();
        control.quickSort(tipo);
        long finalTime = System.currentTimeMillis() - starTime;
        System.out.println("" + finalTime);
        JOptionPane.showMessageDialog(null, "Tiempo demora metodo QuickSort: " + finalTime, "OK", JOptionPane.INFORMATION_MESSAGE);
    }
    
    public void tiempoDemoraMergeSort(Integer tipo){
        
        long starTime = System.currentTimeMillis();
        control.mergeSort(tipo);
        long finalTime = System.currentTimeMillis() - starTime;
        System.out.println("" + finalTime);
        JOptionPane.showMessageDialog(null, "Tiempo demora metodo MergeSort: " + finalTime, "OK", JOptionPane.INFORMATION_MESSAGE);
    }
    
